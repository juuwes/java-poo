// Programa para ler os dados de funcionários. Mostrar os dados dos funcionários e aumentar o salário do funcionário com base em uma porcentagem dada.

package entities;

public class Employee {

	public String name;
	public double grossSalary;
	public double tax;

	public double netSalary() {
		return grossSalary - tax;
	}

	public void increaseSalaray(double percentage) {
		grossSalary *= (percentage + 100.0) / 100.0;
	}

	public String toString() {
		return name + ", $ " + String.format("%.2f%n", netSalary());
	}
}
